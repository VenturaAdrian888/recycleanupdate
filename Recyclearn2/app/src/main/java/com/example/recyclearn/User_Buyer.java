package com.example.recyclearn;

public class User_Buyer {
    public String fullName, username, phonenumber, location, email, password;

    public User_Buyer() {

    }

    public User_Buyer(String fullName, String username, String phonenumber, String location, String email, String password) {
        this.fullName = fullName;
        this.username = username;
        this.phonenumber = phonenumber;
        this.location = location;
        this.email = email;
        this.password = password;

    }
}
